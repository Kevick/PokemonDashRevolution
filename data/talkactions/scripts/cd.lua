function cooldowncheck(cid, storage)
if exhaustion.get(cid, storage) ~= false and exhaustion.get(cid, storage) > 0 then
return "Wait "..math.floor(exhaustion.get(cid, storage)+1).." seconds to use."
end
return "ready."
end

