             end
          end
       elseif isPokeball(item.itemid) then
          table.insert(ret, item)
       end
   end
end
return ret
end

function setPokeballInfo(pokeball, name, hp, maxhp, nick)
    if (pokeball == nil) then return true end
    if (hp == nil) then hp = (pokes[name].cons * HPperVITsummon) end
    if (maxhp == nil) then maxhp = (pokes[name].cons * HPperVITsummon) end
	if nick then
	    doItemSetAttribute(pokeball, "nick", nick)
	end
    
	doItemSetAttribute(pokeball, "poke", "This is "..name.."'s pokeball. HP = ["..hp.."/"..maxhp.."]")
	doItemSetAttribute(pokeball, "php", (hp / maxhp))
end

function getPokeballInfo(item)
	    local name = getItemAttribute(item, "poke"):match("This is (.-)'s pokeball.")
		
		local health, healthmax = getItemAttribute(item, "poke"):match("pokeball. HP = %[(%d+)/(%d+)%]")
		if health ~= nil and healthmax ~= nil then
			health, healthmax = tonumber(health), tonumber(healthmax)
			healthmax = pokes[name].vida
		end
		
		local nick = getItemAttribute(item, "nick")
		if (nick == nil) then
		    nick = name
		else
		    nick = getItemAttribute(item, "nick")
		end
		
		local boost = getItemAttribute(item, "boost")
		if boost then
		    boost = getItemAttribute(item, "boost")
		else
		    boost = 0
        end
		local gender = getItemAttribute(item, "gender")
		if gender then
		    gender = getItemAttribute(item, "gender")
		else
		    gender = 0
		end
return {name=name, nick=nick, hp=health, maxhp=maxhealth, boost=boost, gender=gender}
end

